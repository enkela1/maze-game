package de.tum.cit.fop.maze;

public class GameObject {
    float x;
    float y;
    int health;
    float speed;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getSpeed() {
        return speed;
    }
}
